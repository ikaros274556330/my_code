# 2)% - 求余数

# 应用1:判断整数的奇偶性 - 判断这个数对2取余的结果是否为0

num = 100
if num%2 == 0:
    print('偶数')
else:
    print('奇数')

# 应用2:取低位数,例如:获取一个整数的个位数 - 数字%10
num = 123
print(num % 10)   # 取个位
print(num % 100 // 10)  # 取十位

# 3)// - 整数(商只取整除部分)
# 注意:负数整除除不尽的时候向下取整 -5//2 == -3
print(5//2)     # 整数取整
print(3.9//3)   # 小数保留小数

# 4)** - 幂运算:x ** y -- 求x的y次方
print(2 ** 3)
print(2**(1/2))     # 开方


