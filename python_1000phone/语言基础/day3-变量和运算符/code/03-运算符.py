# 3.逻辑运算符:and(与)、or(或)、not(非)
# 逻辑运算符的运算对象和结果一般都是布尔值
# 1)and
"""
运算规则:两个都是True结果才是True,只要一个是False结果就是False
True and True -> True
True and False -> False
False and True -> False
False and False -> False
使用场景:需要多个条件同时满足的时候,就用and,
"""

gpa = 4.5
score = 90

# 获取奖学金的条件:绩点不低于4并且操评分高于95
print('是否能获取奖学金:',gpa >= 4 and score > 95)

# 练习:判断一个数是否能同时被3和7整数
print('35可以被3和7整除', 35 % 7 == 0 and 35 % 3 == 0)
print('21不能被3和7整除', 21 % 7 == 0 and 21 % 3 == 0)

# 2)or
"""
运算规则:两个都是False结果才是False,只要有一个True结果就是True
True or True -> True
True or False -> True
False or True -> True
False or False -> False
使用场景:多个条件中有一个条件满足就行,相当于生活中的或者
"""

# 3)not
"""
运算规则:True变False,False变True
not True -> False
not False -> True
使用场景:对某一个条件进行否定
"""

# 一个数不能同时被3和7整除的条件
num = 21
print('不能同时被3和7整除:', not (num % 3 == 0 and num % 7 == 0))

# 4)短路操作
"""
and的短路操作: 条件1 and 条件2 -> and前面的条件如果是False,那么后面的条件语句不会执行,结果直接是False
or的短路操作:条件1 or 条件2 -> or前面的条件如果是True,那么后面的条件语句不会执行,结果直接是True
"""

